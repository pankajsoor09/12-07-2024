
public class Person 
	{
	 private String name;
	 private String address;
	 private String dob;

	public void setName(String n)
	{
	 name = n;
	}
	public String getName()
	{
	return  name;
	}
	public void setAddress(String a)
	{
	 address = a;
	}
	public String getAddress()
	{
	return address;
	}
	public void setDob(String d)
	{
	dob = d;
	}
	public String getDob()
	{
	return dob;
	}

}


